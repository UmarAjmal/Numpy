# sort() - ndarray has function that is called sort, and this will sort specify array.
import numpy as np
arr = np.array([1, 12, 8, 4, 5, 6, 10, 3, 9, 7, 11, 2, 0 ])
arr.sort() #this methord is just like copy() methord.
print(arr)

